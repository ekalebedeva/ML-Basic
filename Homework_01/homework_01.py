# Задача 1. Пользователь в бесконечном цикле вводит числа через консоль до тех пор, пока не введёт что-то, отличное от числа (после этого цикл завершается). В конце программа должна вывести сумму всех введённых чисел.

total = 0
while True:
    user_input = input("Введите число: ")
    if user_input.isdigit():
        total += int(user_input)
    else:
        print("Вы ввели не число. Цикл остановлен.")
        break
print("Сумма всех введённых чисел:", total)

# Задача 2. Пользователь вводит строку через консоль (состоящую только из букв в разном регистре). Программа должна создать новую строку, содержащую только те буквы из исходной строки, которые были в верхнем регистре.

user_input = input("Введите строку с буквами разного регистра: ")
upper_letters = ""
for letter in user_input:
    if letter.isupper():
        upper_letters += letter
print("Буквы в верхнем регистре:", upper_letters)

# Задача 3. Классическая игра FizzBuzz. Программа в цикле выводит в консоль числа от 1 до 100.
# Если число кратно 3, вместо него выводится Fizz.
# Если кратно 5 — Buzz.
# Если кратно и 3, и 5 — FizzBuzz.

for i in range(1, 101):
    if i % 3 == 0 and i % 5 == 0:
        print("FizzBuzz")
    elif i % 3 == 0:
        print("Fizz")
    elif i % 5 == 0:
        print("Buzz")
    else:
        print(i)
# Задача 4. Пользователь вводит строку, содержащую любые символы (буквы, цифры и специальные символы).
# Программа должна вычислить и вывести сумму всех цифр, присутствующих в этой строке.

user_input = input("Введите строку с любыми символами: ")
total = 0
for char in user_input:
    if char.isdigit():
        total += int(char)
print("Сумма всех цифр в строке:", total)

# Задача 5. Пользователь вводит через консоль 10 чисел.
# Программа должна определить максимальное число и вывести его в консоль.

max_number = float('-inf')
for i in range(10):
    number = int(input("Введите число: "))
    if number > max_number:
        max_number = number
print("Максимальное число:", max_number)
